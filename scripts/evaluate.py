#!/usr/bin/env python3
"""
evaluate.py -- compute PER and PFER for every model against the references.

Reads:
    references.tsv          (filename, language, family, reference_ipa)
    results/<model>/*.txt   (one IPA hypothesis per audio file)

Writes:
    per_file_results.tsv    every file x model, with PER and PFER (strict + folded)
    summary_by_model.tsv    mean PER + PFER per model (strict + folded)
    summary_by_family.tsv   mean PER + PFER per model per language family

Run in an environment with:  pip install jiwer panphon
From the project root:        python scripts/evaluate.py

Metric notes:
- PER: edit distance over phones, normalized by reference length. jiwer treats
  whitespace-separated tokens as units, so phones must be space-separated.
- PFER: panphon feature edit distance (substitution cost scales with the number
  of differing articulatory features; insert/delete cost 1), normalized by the
  number of reference phones. Matches the spirit of Taguchi & Sakai (2023).

Two granularities are reported for BOTH metrics:
- *strict*  -- scored on the raw narrow IPA (length marks, devoicing, dentals...).
- *folded*  -- scored after fold_to_broad() removes diacritics/length/tone, so a
  narrow reference like [iː] or [b̥] collapses to the broad phone [i] / [b] that
  most models actually emit. The strict-vs-folded gap quantifies how much of our
  high PER is pure reference-granularity mismatch rather than recognition error
  (see guide section 9). The folded numbers are an ADDITIONAL diagnostic metric;
  they do NOT replace the strict ones.

Cleaning notes (see guide section 9): model outputs are normalized to canonical
Unicode and stripped of special tokens before scoring. Extend clean_ipa() as you
discover model-specific quirks.
"""

import csv
import os
import re
import statistics
import unicodedata

import jiwer
import panphon.distance

RESULTS_DIR = "results"            # one subfolder of *.txt hypotheses per model
REFERENCES = "references.tsv"      # gold IPA, regenerated by the dataset builder

# panphon's Distance object: provides both the feature-edit-distance metric and
# the IPA segmenter (dst.fm.ipa_segs) we use to count/split phones. Constructed
# once at import because building its feature table is relatively expensive.
dst = panphon.distance.Distance()

# Look-alike code points that should be unified before scoring, so that a model
# writing an ASCII stand-in is not penalised against the IPA reference glyph.
LOOKALIKE_MAP = {
    "g": "ɡ",   # LATIN SMALL LETTER G -> IPA SCRIPT G
    ":": "ː",   # colon -> IPA length mark
    "'": "ˈ",   # apostrophe -> primary stress
}

# Matches model control tokens that are not phones, e.g. <|en|>, <pad>, </s>.
# These are deleted (replaced with a space) before scoring.
SPECIAL_TOKEN_RE = re.compile(r"<\|?[^>|]*\|?>")

# Unicode general categories that the "broad" fold strips out. Working on the
# NFD (fully decomposed) form, this single rule removes the whole diacritic zoo:
#   Mn -- nonspacing combining marks: dentals [t̪], devoicing [b̥], nasalization,
#         the affricate tie bar [t͡s], etc.
#   Lm -- spacing modifier letters: length [ː], half-length [ˑ], stress [ˈ ˌ],
#         aspiration [ʰ], secondary articulation [ʷ ʲ ˠ ˤ], rhoticity [˞], ...
#   Sk -- modifier symbols: tone-bar letters [˥ ˦ ˧ ...].
# What survives is the bare base-letter phone -- the "broad" transcription level
# that the broad-trained models target.
FOLD_CATEGORIES = {"Mn", "Lm", "Sk"}


def clean_ipa(text):
    """Normalize a raw model output (or reference) to canonical IPA for scoring.

    Steps: drop special tokens -> NFD-decompose (so diacritics become their own
    code points, which both look-alike mapping and folding rely on) -> map ASCII
    look-alikes to their IPA equivalents -> collapse runs of whitespace.
    """
    if not text:
        return ""                                       # guard against None/""
    text = SPECIAL_TOKEN_RE.sub(" ", text)              # strip <|...|>, <pad>, ...
    text = unicodedata.normalize("NFD", text)           # split base + diacritics
    text = "".join(LOOKALIKE_MAP.get(ch, ch) for ch in text)  # ASCII -> IPA glyphs
    text = re.sub(r"\s+", " ", text).strip()            # collapse whitespace
    return text


def fold_to_broad(text):
    """Collapse narrow IPA to broad phones by deleting diacritics/length/tone.

    Decompose to NFD first, then drop every code point whose Unicode category is
    in FOLD_CATEGORIES (combining marks, modifier letters, tone-bar symbols). The
    result keeps only base-letter phones, so e.g. [iː]->[i], [b̥]->[b], [t̪]->[t],
    [t͡s]->[ts]. Used to build the *folded* PER/PFER variant. Caveat: tie-bar
    affricates become two base letters and thus two phones -- acceptable because
    the same fold is applied identically to reference and hypothesis.
    """
    decomposed = unicodedata.normalize("NFD", text)     # expose combining marks
    kept = [                                            # keep base letters only
        ch for ch in decomposed
        if unicodedata.category(ch) not in FOLD_CATEGORIES
    ]
    folded = "".join(kept)
    return re.sub(r"\s+", " ", folded).strip()          # tidy whitespace again


def to_phone_tokens(text):
    """Return a space-separated phone string for PER (jiwer needs delimiters).

    jiwer counts whitespace-separated tokens as edit units, so each phone must be
    its own token. If the input already has spaces we trust them (Allosaurus and
    other models emit space-separated phones); otherwise we segment the string
    into phones with panphon so the error rate is over phones, not characters.
    """
    cleaned = clean_ipa(text)
    if " " in cleaned:
        return cleaned                                  # already phone-separated
    # No spaces: ask panphon to split the run of glyphs into IPA segments.
    segs = dst.fm.ipa_segs(cleaned.replace(" ", ""))
    return " ".join(segs) if segs else cleaned          # fall back to raw string


def load_references():
    """Read references.tsv -> {file_key: {language, family, reference_ipa}}.

    The file_key is the filename without extension, matching how each model's
    output .txt files are named, so hypotheses can be joined to references.
    """
    refs = {}
    with open(REFERENCES, encoding="utf-8") as f:
        for row in csv.DictReader(f, delimiter="\t"):   # TSV -> dict per row
            key = os.path.splitext(row["filename"])[0]  # drop ".wav"
            refs[key] = {
                "language": row["language"],
                "family": row["family"],
                "reference_ipa": row["reference_ipa"].strip(),
            }
    return refs


def discover_models():
    """Each subfolder of results/ is one model's hypotheses; return their names."""
    return sorted(
        d for d in os.listdir(RESULTS_DIR)
        if os.path.isdir(os.path.join(RESULTS_DIR, d))  # ignore stray log files
    )


def load_hypothesis(model, key):
    """Read one model's IPA output for file `key`, or None if it was not produced."""
    path = os.path.join(RESULTS_DIR, model, key + ".txt")
    if not os.path.exists(path):
        return None                                     # model skipped this file
    with open(path, encoding="utf-8") as f:
        return f.read().strip()


def compute_per(reference, hypothesis, fold=False):
    """Phone Error Rate: jiwer edit distance over space-separated phones / ref length.

    With fold=True both sides are first reduced to broad phones, giving the
    diacritic-folded PER variant. Returns None when the reference is empty.
    """
    if fold:                                            # broad variant: pre-fold
        reference = fold_to_broad(reference)
        hypothesis = fold_to_broad(hypothesis)
    ref_tokens = to_phone_tokens(reference)             # space-separated phones
    hyp_tokens = to_phone_tokens(hypothesis)
    if not ref_tokens:                                  # nothing to score against
        return None
    return jiwer.wer(ref_tokens, hyp_tokens)            # phone-level edit distance


def compute_pfer(reference, hypothesis, fold=False):
    """Phonological Feature ER: panphon feature edit distance / number of ref phones.

    With fold=True both sides are reduced to broad phones first. panphon wants a
    continuous (unspaced) IPA string, so we strip the scoring spaces here.
    """
    if fold:                                            # broad variant: pre-fold
        reference = fold_to_broad(reference)
        hypothesis = fold_to_broad(hypothesis)
    ref = clean_ipa(reference).replace(" ", "")         # panphon: no separators
    hyp = clean_ipa(hypothesis).replace(" ", "")
    if not ref:
        return None
    fed = dst.feature_edit_distance(ref, hyp)           # feature-weighted distance
    n_phones = len(dst.fm.ipa_segs(ref)) or 1           # ref phone count (>=1)
    return fed / n_phones                               # normalise per phone


def _mean(values):
    """Mean formatted to 4 dp, or "" for an empty list (keeps TSV cells aligned)."""
    return f"{statistics.mean(values):.4f}" if values else ""


def main():
    refs = load_references()                            # gold transcriptions
    models = discover_models()                          # one folder per model
    if not models:
        raise SystemExit(f"No model folders found in {RESULTS_DIR}/")
    print(f"Models found: {', '.join(models)}")

    per_file_rows = []                                  # one row per (file, model)
    # model -> list of metric tuples (per, pfer, per_folded, pfer_folded)
    results = {m: [] for m in models}
    # (model, family) -> same metric tuples, for the per-family breakdown
    family_results = {}

    # ---- score every reference against every model's hypothesis -------------
    for key, ref in refs.items():
        for model in models:
            hyp = load_hypothesis(model, key)
            if hyp is None:                             # this model lacks the file
                print(f"  WARNING: no output for {model}/{key}")
                continue
            gold = ref["reference_ipa"]
            per = compute_per(gold, hyp)                # strict, narrow IPA
            pfer = compute_pfer(gold, hyp)
            per_f = compute_per(gold, hyp, fold=True)   # folded, broad phones
            pfer_f = compute_pfer(gold, hyp, fold=True)
            per_file_rows.append({
                "filename": key, "language": ref["language"],
                "family": ref["family"], "model": model,
                "PER": f"{per:.4f}" if per is not None else "",
                "PFER": f"{pfer:.4f}" if pfer is not None else "",
                "PER_folded": f"{per_f:.4f}" if per_f is not None else "",
                "PFER_folded": f"{pfer_f:.4f}" if pfer_f is not None else "",
            })
            # only aggregate rows where every metric is defined
            if None not in (per, pfer, per_f, pfer_f):
                tup = (per, pfer, per_f, pfer_f)
                results[model].append(tup)
                family_results.setdefault((model, ref["family"]), []).append(tup)

    # ---- per-file detail TSV -----------------------------------------------
    with open("per_file_results.tsv", "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, delimiter="\t",
                           fieldnames=["filename", "language", "family", "model",
                                       "PER", "PFER", "PER_folded", "PFER_folded"])
        w.writeheader()
        w.writerows(per_file_rows)

    # ---- mean per model -----------------------------------------------------
    with open("summary_by_model.tsv", "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f, delimiter="\t")
        w.writerow(["model", "n", "mean_PER", "mean_PFER",
                    "mean_PER_folded", "mean_PFER_folded"])
        for model in models:
            vals = results[model]                       # list of metric tuples
            w.writerow([model, len(vals),
                        _mean([v[0] for v in vals]),    # mean strict PER
                        _mean([v[1] for v in vals]),    # mean strict PFER
                        _mean([v[2] for v in vals]),    # mean folded PER
                        _mean([v[3] for v in vals])])   # mean folded PFER

    # ---- mean per model per language family --------------------------------
    with open("summary_by_family.tsv", "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f, delimiter="\t")
        w.writerow(["model", "family", "n", "mean_PER", "mean_PFER",
                    "mean_PER_folded", "mean_PFER_folded"])
        for (model, family), vals in sorted(family_results.items()):
            w.writerow([model, family, len(vals),
                        _mean([v[0] for v in vals]),
                        _mean([v[1] for v in vals]),
                        _mean([v[2] for v in vals]),
                        _mean([v[3] for v in vals])])

    print("\nWrote: per_file_results.tsv, summary_by_model.tsv, summary_by_family.tsv")
    print("\nQuick summary by model (strict | folded):")
    for model in models:
        vals = results[model]
        if vals:
            print(f"  {model:18s} "
                  f"PER={statistics.mean(v[0] for v in vals):.3f} "
                  f"PFER={statistics.mean(v[1] for v in vals):.3f} | "
                  f"PER_f={statistics.mean(v[2] for v in vals):.3f} "
                  f"PFER_f={statistics.mean(v[3] for v in vals):.3f} (n={len(vals)})")


if __name__ == "__main__":
    main()
